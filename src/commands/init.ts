import { CommandResult } from '../types/command.js';

const SHELL_HOOK = [
  '_env_switcher_bin() {',
  '  if [ -n "$ENV_SWITCHER_BIN" ]; then',
  '    echo "$ENV_SWITCHER_BIN"',
  '    return',
  '  fi',
  '  local cmd',
  '  cmd="$( (unset -f env-switcher 2>/dev/null; type -p env-switcher) 2>/dev/null)"',
  '  if [ -n "$cmd" ] && [ -x "$cmd" ]; then',
  '    echo "$cmd"',
  '    return',
  '  fi',
  '  cmd="$( (unset -f env-switcher 2>/dev/null; command -v env-switcher) 2>/dev/null)"',
  '  if [ -n "$cmd" ] && [ -x "$cmd" ]; then',
  '    echo "$cmd"',
  '    return',
  '  fi',
  '    echo ""',
  '}',
  '',
  'env-switcher() {',
  '  local bin',
  '  if [ -n "$ENV_SWITCHER_BIN" ]; then',
  '    bin="$ENV_SWITCHER_BIN"',
  '  else',
  '    bin="$(_env_switcher_bin)"',
  '  fi',
  '  if [ -z "$bin" ] || [ ! -x "$bin" ]; then',
  '    echo "错误: env-switcher 未安装或不在 PATH 中" >&2',
  '    return 1',
  '  fi',
  '  if [ "$1" = "switch" ]; then',
  '    local profile="${2:-}"',
  '    if [ -z "$profile" ]; then',
  '      # 无参数时，检查是否在 TTY 环境下',
  '      if [ -t 0 ]; then',
  '        # 有 TTY，调用交互式选择',
  '        $bin switch',
  '        if [ $? -ne 0 ]; then',
  '          return 1',
  '        fi',
  '        # 交互式选择后，获取当前 profile 的环境变量并应用',
  '        local export_output',
  '        export_output="$($bin export --current 2>&1)"',
  '        if [ $? -ne 0 ]; then',
  '          echo "错误: $export_output" >&2',
  '          return 1',
  '        fi',
  '        eval "$export_output"',
  '      else',
  '        echo "错误: 交互式切换需要在终端中运行" >&2',
  '        return 1',
  '      fi',
  '    else',
  '      local export_output',
  '      export_output="$($bin switch "$profile" 2>&1)"',
  '      if [ $? -ne 0 ]; then',
  '        echo "错误: $export_output" >&2',
  '        return 1',
  '      fi',
  '      eval "$export_output"',
  '      echo "已切换到配置: $profile"',
  '    fi',
  '  elif [ "$1" = "--pick-current" ]; then',
  '    $bin export --current',
  '  elif [ "$1" = "export" ] && [ "$2" = "--current" ]; then',
  '    $bin export --current',
  '  elif [ "$1" = "export" ]; then',
  '    shift',
  '    $bin export "$@"',
  '  else',
  '    $bin "$@"',
  '  fi',
  '}',
].join('\n');

export async function initCommand(): Promise<CommandResult> {
  return {
    success: true,
    output: SHELL_HOOK,
  };
}
